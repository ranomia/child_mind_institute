import os
import pandas as pd

class UseColumnExtractor:
    def __init__(self, input_path: str, output_dir: str):
        self.input_path = input_path
        self.output_dir = output_dir
        self.group_col = ''
        self.target_col_list = []
        self.feature_col_list = []
        self.other_target_col_list = []

    def extract_columns(self, df: pd.DataFrame):
        # 必要なカラムを集める
        required_columns = [self.group_col] + self.feature_col_list + self.target_col_list
        
        # 必要なカラムだけを抽出
        filtered_df = df[required_columns]
        
        # target_col_listの各要素について個別のDataFrameを生成
        target_dfs = {
            target: filtered_df[[self.group_col] + self.feature_col_list + [target]]
            for target in self.target_col_list
        }
        return target_dfs

    def load_data(self, input_path: str) -> pd.DataFrame:
        """
        データを読み込む

        :return: データフレーム
        """
        # ファイル拡張子を取得
        file_extension = input_path.split('.')[-1].lower()

        # 拡張子に基づいて処理を分岐
        if file_extension in ['pkl', 'pickle']:
            df = pd.read_pickle(input_path)
        elif file_extension == 'csv':
            df = pd.read_csv(input_path)
        elif file_extension == 'xlsx':
            df = pd.read_excel(input_path)
        elif file_extension == 'parquet':
            df = pd.read_parquet(input_path)
        else:
            # 未対応の形式に対するエラーを発生させる
            raise ValueError(f"Unsupported file format: '{file_extension}'. Supported formats are: pkl, pickle, csv, xlsx, parquet.")

        return df
    
    def save_data(self, df: pd.DataFrame, output_path: str) -> None:
        """
        データフレームをjson linesで保存する
        """
        df.to_json(output_path, force_ascii=False, lines=True, orient='records')

    def forward(self):
        self.set_list()

        df = self.load_data(self.input_path)
        dict_df_extracted = self.extract_columns(df)
        
        for target_col, df_extracted in dict_df_extracted.items():
            output_path = os.path.join(self.output_dir, f'train_extracted_{target_col}.jsol')
            self.save_data(
                 df = df_extracted
                ,output_path = output_path
            )

